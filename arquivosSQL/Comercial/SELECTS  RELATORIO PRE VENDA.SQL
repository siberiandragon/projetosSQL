select 
 PCPEDC.NUMPED,
  PCPEDC.CODFILIAL,
   PCCLIENT.CODCLI,
    PCPEDC.CODUSUR,
     PCUSUARI.NOME,
      PCCLIENT.DTCADASTRO,
      PCCLIENT.CLIENTE,
       PCCLIENT.CGCENT,
        PCCLIENT.EMAIL,
         PCCLIENT.TELENT,
          PCCLIENT.ENDERENT,
           PCCLIENT.BAIRROENT,
            PCCLIENT.MUNICENT,
             PCCLIENT.ESTENT,
              PCCLIENT.CEPENT,
               PCPEDC.VLTOTAL,
                 PCPEDC.DATA
from PCCLIENT  
join PCPEDC on PCCLIENT.CODCLI = PCPEDC.CODCLI
join PCUSUARI on PCPEDC.CODUSUR = PCUSUARI.CODUSUR
where PCPEDC.CODFILIAL in(:CODFILIAL)
and PCUSUARI.CODUSUR =(:CODUSUR)
and PCCLIENT.CODCLI =(:CODCLI)
and PCPEDC.DATA between :DATINI and :DATFIM
and PCPEDC.VLTOTAL >'0'
and PCPEDC.POSICAO='F';















