select * from PCPRODUT;


--Consulta de estoque de produtos todas as filiais
select PCEST.CODFILIAL, PCEST.CODPROD, PCPRODUT.DESCRICAO, PCEST.QTEST as ESTOQUE, PCPRODUT.NBM as NCM, PCPRODUT.CODFAB from PCEST
join PCPRODUT on PCEST.CODPROD = PCPRODUT.CODPROD 
where PCPRODUT.NBM='85176294' and PCEST.CODFILIAL in('2','3','4')
order by  QTEST;

--Consulta de estoque de produto por filial
select PCEST.CODFILIAL, PCEST.CODPROD, PCPRODUT.DESCRICAO, PCEST.QTEST as ESTOQUE, PCPRODUT.NBM as NCM, PCPRODUT.CODFAB from PCEST
join PCPRODUT on PCEST.CODPROD = PCPRODUT.CODPROD 
where PCPRODUT.NBM='85447010' and PCEST.CODFILIAL='4'
order by  QTEST;

--Consulta de NCM dos produtos todas as filiais
